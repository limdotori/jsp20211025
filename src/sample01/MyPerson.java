package sample01;

public class MyPerson {
private String name;
private int Age;


public MyPerson(String name, int age) {
	super();
	this.name = name;
	Age = age;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getAge() {
	return Age;
}
public void setAge(int age) {
	Age = age;
}

}
